//Basic data Types:

#include<iostream>
using namespace std;

int main() {
    int a = 10;
    float b = 5.5;
    double c = 10.12345;
    string d= "Programming";
    char e = 'X';
    bool f = true;

    cout <<"Integer type value:"<< a << endl;

    cout <<"Float type value:"<<  b << endl;

    cout <<"Double type value:"<<c << endl;

    cout <<"String type value:"<<d<< endl;

    cout <<"Character type value:"<<e<< endl;

    cout <<"Boolean type value:"<<  f;

    return 0;
}
